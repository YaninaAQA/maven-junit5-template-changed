package org.example;

public class MathUtils {
    public int add(int a, int b) {
        return a + b;
    }

    public boolean greater(int a, int b) {
        return a > b;
    }

    public float divide(float a, float b) {
        return a / b;
    }

    public float multiple(float a, float b) {
        return a * b;
    }

    public float square(float a) {
        return a * a;
    }
}
